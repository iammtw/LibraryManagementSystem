    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String Username = username.getText();
            String sql = "SELECT * from users where username = '"+Username+"'";
            PreparedStatement st = conn.prepareStatement(sql);
            ResultSet rs =  st.executeQuery();
            if(rs.next()){
                name.setText(rs.getString(2));
                sec_q.setText(rs.getString(5));
                username.setEditable(false);
            } else {
                JOptionPane.showMessageDialog(null,"User Not Found");
            }
            //name.setText(Answer);
            // database code
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,ex);
        } 
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try{
            String Answer = answer.getText();
            String Username = username.getText();
            String sql = "SELECT * from users where username = ? and answer = ?";
            PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, Username);
            st.setString(2, Answer);
            ResultSet rs =  st.executeQuery();
            if(rs.next()){
                password.setText(rs.getString(4));
                answer.setEditable(false);
            } else {
                JOptionPane.showMessageDialog(null,"Wrong Answer! Please Try Again.");
            }
            //name.setText(Answer);
            // database code
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,ex);
        } 
        
    }//GEN-LAST:event_jButton2ActionPerformed

