    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        String Name = name.getText();
        String Username = username.getText();
        String Password = password.getText();
        String Security_question = ((String)sec_q.getSelectedItem());
        String Answer = answer.getText();
        try{
            String sql = "INSERT into users(name,username,password,sec_q,answer) values(?,?,?,?,?)";
            PreparedStatement st = conn.prepareStatement(sql);
            
            st.setString(1, Name);
            st.setString(2, Username);
            st.setString(3, Password);
            st.setString(4, Security_question);
            st.setString(5, Answer);
            st.execute();
            JOptionPane.showMessageDialog(null, "Registered Successfully!!");
            setVisible(false);
            new Login().setVisible(true);
            
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Signup.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
//        System.out.println("Name is: "+Name+"\nUsername is "+Username+"\nPassword is: "+Password+"\nSecurity "+Security_question+"\nAnswer is "+ Answer);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void nameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nameKeyTyped
        char c = evt.getKeyChar();
        if(!(Character.isLetter(c)) || (c==KeyEvent.VK_BACKSPACE) || (c == KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_nameKeyTyped

